#!/usr/bin/env node
/**
 * Reads selected tile PNGs from the Modern Office Revamped asset pack
 * and generates a TypeScript module with named base64 data URIs.
 *
 * If the asset pack folder is not found, generates an empty module
 * so the build still succeeds (procedural fallback kicks in).
 */

const fs = require('fs');
const path = require('path');

const SINGLES_DIR = path.join(
  __dirname, '..', 'Modern_Office_Revamped_v1.2',
  '4_Modern_Office_singles', '48x48'
);
const OUT_FILE = path.join(__dirname, '..', 'src', 'webview', 'tileAssets.ts');

/**
 * Map: sprite name → singles file number.
 * These are hand-picked from the 339 available singles to cover
 * all tile types in our office layout.
 */
const TILE_MAP = {
  // Floor variants (zone-based)
  floor_gray:     91,
  floor_wood:     89,
  floor_carpet:   87,
  floor_tile:     91,

  // Furniture (drawn on top of floor)
  chair_front:    101,
  plant_bushy:    98,
  plant_tall:     100,
  server_rack:    280,
  water_cooler:   310,
  coffee_station: 320,

  // Wall decorations (drawn on top of wall)
  wall_art_1:     96,
  wall_art_2:     97,
  wall_chart:     113,
  whiteboard_img: 170,

  // Desk items (composited onto desk surface)
  monitor_front:  133,
  keyboard:       116,
  desk_lamp:      145,
  laptop:         140,
  printer:        150,
};

function generate() {
  // If asset pack not found, generate empty module (procedural fallback)
  if (!fs.existsSync(SINGLES_DIR)) {
    const empty = [
      '/** Auto-generated by scripts/generate-tile-data.js — no asset pack found */',
      '/** Procedural rendering will be used as fallback */',
      '',
      'export const TILE_SPRITE_URIS: Readonly<Record<string, string>> = {};',
      '',
    ].join('\n');

    fs.writeFileSync(OUT_FILE, empty);
    console.log(`Generated empty ${OUT_FILE} (asset pack not found at ${SINGLES_DIR})`);
    return;
  }

  const lines = [
    '/** Auto-generated by scripts/generate-tile-data.js — do not edit */',
    '/** Base64 data URIs for Modern Office tile sprites */',
    '',
  ];

  const entries = [];
  let count = 0;

  for (const [name, num] of Object.entries(TILE_MAP)) {
    const filename = `Modern_Office_Singles_48x48_${num}.png`;
    const pngPath = path.join(SINGLES_DIR, filename);

    if (!fs.existsSync(pngPath)) {
      console.warn(`Warning: Missing ${filename}, skipping ${name}`);
      continue;
    }

    const buf = fs.readFileSync(pngPath);
    const b64 = buf.toString('base64');
    entries.push(`  ${name}: 'data:image/png;base64,${b64}'`);
    count++;
  }

  lines.push('export const TILE_SPRITE_URIS: Readonly<Record<string, string>> = {');
  lines.push(entries.join(',\n'));
  lines.push('};');
  lines.push('');

  fs.writeFileSync(OUT_FILE, lines.join('\n'));
  console.log(`Generated ${OUT_FILE} (${count} tiles from asset pack)`);
}

generate();
